package data;

/**
 * Class representing the different Java types
 */
public enum DefinitionTypes {
	/**
	 * All possible types
	 */
	ALL,
	/**
	 * Classes which are not part of the java package
	 */
	EXTERN,
	/**
	 * Default Java classes
	 */
	JAVA_CLASSES,
	/**
	 * Primitive Datatypes
	 */
	PRIMITIVE;
}
